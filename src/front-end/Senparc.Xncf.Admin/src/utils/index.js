/**
 * MD5-Cloud services
 * @param {file} file
 */
export function computeMD5(file) {
  return new Promise((resolve, reject) => {
    // resolve(new Date().getTime() + '' + file.lastModified + '' + file.uid + '' + Math.random())
    // 缺少SparkMD5包依赖包，暂用此方法-后期调整
    const blobSlice = File.prototype.slice || File.prototype.mozSlice || File.prototype.webkitSlice
    const chunkSize = 2097152 // 体积大小-2MB
    const chunks = Math.ceil(file.size / chunkSize)
    let currentChunk = 0
    const spark = new SparkMD5.ArrayBuffer()
    const fileReader = new FileReader()
    // const time = new Date().getTime();
    file.cmd5 = true // 文件状态为“计算md5...”
    fileReader.onload = (e) => {
      spark.append(e.target.result) // Append array buffer
      currentChunk++
      if (currentChunk < chunks) {
        console.log(`第${currentChunk}分片解析完成, 开始第${currentChunk + 1} / ${chunks}分片解析`);
        loadNext()
      } else {
        // console.log('finished loading');
        const md5 = spark.end() // 得到md5
        console.log(`MD5计算完成：${file.name} \nMD5：${md5} \n分片：${chunks} 大小:${file.size} 用时：${new Date().getTime() - time} ms`);
        spark.destroy() // 释放缓存
        file.uniqueIdentifier = md5 // 将文件md5赋值给文件唯一标识
        file.cmd5 = false // 取消计算md5状态
        // file.resume(); // 开始上传-这里逻辑需要注释
        resolve(md5)
      }
    }
    fileReader.onerror = (err) => {
      reject(err)
      console.warn('oops, something went wrong.')
      file.cancel()
    }
    const loadNext = () => {
      const start = currentChunk * chunkSize
      const end = ((start + chunkSize) >= file.size) ? file.size : start + chunkSize
      fileReader.readAsArrayBuffer(blobSlice.call(file, start, end))
    }
    loadNext()
  })
}

/**
 * uuid-Cloud services
 * @param {len} len
 * @param {radix} radix
 */
export function uuid(len, radix) {
  const chars = '0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'.split('')
  const uuid = []
  let i
  radix = radix || chars.length

  if (len) {
    // Compact form
    for (i = 0; i < len; i++) uuid[i] = chars[0 | Math.random() * radix]
  } else {
    // rfc4122, version 4 form
    var r
    // rfc4122 requires these characters
    uuid[8] = uuid[13] = uuid[18] = uuid[23] = '-'
    uuid[14] = '4'

    // Fill in random data.  At i==19 set the high bits of clock sequence as
    // per rfc4122, sec. 4.1.5
    for (i = 0; i < 36; i++) {
      if (!uuid[i]) {
        r = 0 | Math.random() * 16
        uuid[i] = chars[(i === 19) ? (r & 0x3) | 0x8 : r]
      }
    }
  }
  return uuid.join('')//字符串导出
}