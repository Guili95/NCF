export function dividedString(character, string) {
  let dataState = {
    text: string,
  }
  var datas = dataState.text.split(character);
  var containsDuplicate = function (datas) {
    for (let k = 1; k < datas.length; k++) {
      console.log(k, datas[k], datas[k - 1])
      if (datas[k] == datas[k - 1]) return true
    }
    return false
  };

  function arrayUnique(arr) {
    var len = arr.length;
    var res = [];
    for (var i = 0; i < len; i++) {
      if (res.indexOf(arr[i]) === -1) {
        res.push(arr[i]);
      }
    }
    return res;
  }

  if (!containsDuplicate) {
    return datas;
  } else {
    return arrayUnique(datas)
  }
}
